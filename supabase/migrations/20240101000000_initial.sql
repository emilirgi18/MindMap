-- profiles ----------------------------------------------------------------
-- Mirrors auth.users with app-level fields. Approval gates all data access.
create table public.profiles (
  id          uuid        primary key references auth.users(id) on delete cascade,
  email       text        not null,
  full_name   text,
  approved    boolean     not null default false,
  created_at  timestamptz not null default now()
);

comment on column public.profiles.approved is
  'Set to true by admin only. False blocks all RLS policies for the user.';


-- workspaces --------------------------------------------------------------
-- owner_id is nullable so campaign workspaces survive user deletion.
create table public.workspaces (
  id          uuid        primary key default gen_random_uuid(),
  owner_id    uuid        references public.profiles(id) on delete set null,
  type        text        not null check (type in ('personal', 'campaign')),
  name        text        not null,
  created_at  timestamptz not null default now()
);


-- workspace_members -------------------------------------------------------
create table public.workspace_members (
  workspace_id  uuid        not null references public.workspaces(id) on delete cascade,
  user_id       uuid        not null references public.profiles(id)   on delete cascade,
  role          text        not null check (role in ('owner', 'dm', 'player')),
  joined_at     timestamptz not null default now(),
  primary key (workspace_id, user_id)
);


-- notes -------------------------------------------------------------------
-- body column stores the Markdown serialization (persistence format).
-- Yjs operates on the in-memory TipTap document; Markdown is only written
-- on snapshot flush.
-- user_id nullable so notes survive profile deletion (campaign context).
create table public.notes (
  id            uuid        primary key default gen_random_uuid(),
  workspace_id  uuid        not null references public.workspaces(id) on delete cascade,
  user_id       uuid        references public.profiles(id) on delete set null,
  title         text        not null default 'Untitled',
  body          text        not null default '',
  dm_only       boolean     not null default false,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);


-- note_links --------------------------------------------------------------
-- Directional edges written by the [[Wikilink]] TipTap extension.
create table public.note_links (
  id         uuid primary key default gen_random_uuid(),
  source_id  uuid not null references public.notes(id) on delete cascade,
  target_id  uuid not null references public.notes(id) on delete cascade,
  unique (source_id, target_id)
);


-- tags --------------------------------------------------------------------
create table public.tags (
  id       uuid primary key default gen_random_uuid(),
  note_id  uuid not null references public.notes(id) on delete cascade,
  name     text not null,
  unique (note_id, name)
);


-- invites -----------------------------------------------------------------
-- Token-based invite links generated by workspace owner/dm.
-- used_at = null  →  still valid.
create table public.invites (
  id            uuid        primary key default gen_random_uuid(),
  workspace_id  uuid        not null references public.workspaces(id) on delete cascade,
  token         uuid        not null unique default gen_random_uuid(),
  created_by    uuid        not null references public.profiles(id) on delete cascade,
  expires_at    timestamptz not null,
  used_at       timestamptz,
  created_at    timestamptz not null default now()
);


