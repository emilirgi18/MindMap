-- =============================================================================
-- RLS POLICIES
-- =============================================================================
-- Approval gate: every policy that accesses data (except own-profile reads,
-- which are needed for the middleware approval check) requires is_approved().
--
-- All membership checks go through security-definer helpers to avoid the
-- infinite-recursion trap of querying workspace_members from within its
-- own RLS policy.
-- =============================================================================


-- ---------------------------------------------------------------------------
-- profiles
-- ---------------------------------------------------------------------------

-- Own profile is always visible — needed by middleware before approval check
create policy "profiles: own select"
  on public.profiles for select
  using (id = auth.uid());

-- Approved users may read profiles of people in any shared workspace
-- (needed for presence bar, member lists)
create policy "profiles: co-member select"
  on public.profiles for select
  using (
    public.is_approved()
    and exists (
      select 1
      from public.workspace_members m1
      join public.workspace_members m2 on m1.workspace_id = m2.workspace_id
      where m1.user_id = auth.uid()
        and m2.user_id = profiles.id
    )
  );

-- Users may update their own non-sensitive fields (full_name etc.)
-- approved can only be changed via service role (admin)
create policy "profiles: own update"
  on public.profiles for update
  using  (id = auth.uid())
  with check (id = auth.uid());


-- ---------------------------------------------------------------------------
-- workspaces
-- ---------------------------------------------------------------------------

create policy "workspaces: member select"
  on public.workspaces for select
  using (
    public.is_approved()
    and public.is_workspace_member(id)
  );

-- Users may only create campaign workspaces; personal is created by trigger
create policy "workspaces: approved create campaign"
  on public.workspaces for insert
  with check (
    public.is_approved()
    and auth.uid() = owner_id
    and type = 'campaign'
  );

-- Only the owner may rename their workspace (type is guarded by trigger)
create policy "workspaces: owner update"
  on public.workspaces for update
  using (
    public.is_approved()
    and owner_id = auth.uid()
  )
  with check (owner_id = auth.uid());

-- Owners may delete campaign workspaces; personal workspace deletion is
-- handled by the profile-delete trigger
create policy "workspaces: owner delete campaign"
  on public.workspaces for delete
  using (
    public.is_approved()
    and owner_id = auth.uid()
    and type = 'campaign'
  );


-- ---------------------------------------------------------------------------
-- workspace_members
-- ---------------------------------------------------------------------------

create policy "workspace_members: member select"
  on public.workspace_members for select
  using (
    public.is_approved()
    and public.is_workspace_member(workspace_id)
  );

-- Only owner/dm may add members; personal workspaces may not have members added
create policy "workspace_members: owner/dm insert"
  on public.workspace_members for insert
  with check (
    public.is_approved()
    and (select type from public.workspaces where id = workspace_id) = 'campaign'
    and public.workspace_role(workspace_id) in ('owner', 'dm')
  );

-- Only owner may reassign roles; owner cannot demote themselves
create policy "workspace_members: owner update role"
  on public.workspace_members for update
  using (
    public.is_approved()
    and public.workspace_role(workspace_id) = 'owner'
  )
  with check (
    -- Prevent the owner from changing their own role via this path
    not (user_id = (select owner_id from public.workspaces where id = workspace_id)
         and role <> 'owner')
  );

-- Members may leave; owner may remove any member except themselves
create policy "workspace_members: leave or owner kick"
  on public.workspace_members for delete
  using (
    public.is_approved()
    and (
      -- Any member can remove themselves (leave)
      user_id = auth.uid()
      -- Owner can remove others
      or public.workspace_role(workspace_id) = 'owner'
    )
    -- Owner cannot remove their own membership via this policy
    and not (
      user_id = auth.uid()
      and public.workspace_role(workspace_id) = 'owner'
    )
  );


