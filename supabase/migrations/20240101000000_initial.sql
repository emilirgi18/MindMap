-- =============================================================================
-- SECURITY-DEFINER HELPER FUNCTIONS
-- =============================================================================
-- Using security definer breaks potential RLS recursion: these functions read
-- tables directly, bypassing RLS, so they are safe to call from within
-- RLS policy expressions.

-- is_approved -----------------------------------------------------------------
create or replace function public.is_approved()
returns boolean
language sql
security definer
stable
set search_path = public
as $$
  select coalesce(
    (select approved from public.profiles where id = auth.uid()),
    false
  );
$$;


-- is_workspace_member ---------------------------------------------------------
create or replace function public.is_workspace_member(p_workspace_id uuid)
returns boolean
language sql
security definer
stable
set search_path = public
as $$
  select exists (
    select 1 from public.workspace_members
    where workspace_id = p_workspace_id
      and user_id = auth.uid()
  );
$$;


-- workspace_role --------------------------------------------------------------
-- Returns current user's role in the workspace, or NULL if not a member.
create or replace function public.workspace_role(p_workspace_id uuid)
returns text
language sql
security definer
stable
set search_path = public
as $$
  select role
  from public.workspace_members
  where workspace_id = p_workspace_id
    and user_id = auth.uid();
$$;


