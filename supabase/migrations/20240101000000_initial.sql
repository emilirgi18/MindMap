-- updated_at auto-maintenance ------------------------------------------------
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger notes_set_updated_at
  before update on public.notes
  for each row execute function public.set_updated_at();


-- New auth user bootstrap ----------------------------------------------------
-- After a row is inserted into auth.users, atomically create:
--   1. profiles row (approved = false)
--   2. personal workspace
--   3. owner membership for that workspace
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_workspace_id uuid;
  v_full_name    text;
begin
  -- Prefer full_name from OAuth metadata, fall back to email prefix
  v_full_name := coalesce(
    new.raw_user_meta_data->>'full_name',
    new.raw_user_meta_data->>'name',
    split_part(new.email, '@', 1)
  );

  insert into public.profiles (id, email, full_name)
  values (new.id, new.email, v_full_name);

  insert into public.workspaces (owner_id, type, name)
  values (new.id, 'personal', 'My Notes')
  returning id into v_workspace_id;

  insert into public.workspace_members (workspace_id, user_id, role)
  values (v_workspace_id, new.id, 'owner');

  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();


-- Profile delete cleanup -----------------------------------------------------
-- When a profile is deleted (cascades from auth.users delete):
--   • Personal workspace is cascade-deleted
--   • Campaign workspaces persist; owner_id FK is already set null by the FK
-- This trigger exists so we have a clear, explicit deletion of the personal
-- workspace regardless of FK ordering.
create or replace function public.handle_profile_delete()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  delete from public.workspaces
  where owner_id = old.id
    and type = 'personal';

  return old;
end;
$$;

create trigger on_profile_deleted
  before delete on public.profiles
  for each row execute function public.handle_profile_delete();


-- Workspace type immutability ------------------------------------------------
create or replace function public.prevent_workspace_type_change()
returns trigger
language plpgsql
as $$
begin
  if new.type <> old.type then
    raise exception 'workspace.type is immutable';
  end if;
  return new;
end;
$$;

create trigger workspaces_immutable_type
  before update on public.workspaces
  for each row execute function public.prevent_workspace_type_change();


-- Single DM per campaign workspace -------------------------------------------
-- When a member is assigned the 'dm' role, any existing dm in that workspace
-- is demoted to 'player' first.
create or replace function public.enforce_single_dm()
returns trigger
language plpgsql
as $$
begin
  if new.role = 'dm' then
    update public.workspace_members
    set role = 'player'
    where workspace_id = new.workspace_id
      and role = 'dm'
      and user_id <> new.user_id;
  end if;
  return new;
end;
$$;

create trigger workspace_members_single_dm
  before insert or update on public.workspace_members
  for each row execute function public.enforce_single_dm();


-- =============================================================================
-- INVITE REDEMPTION FUNCTION
-- =============================================================================
-- Security definer so it can write to workspace_members and invites
-- atomically without the caller needing broad INSERT permissions.
create or replace function public.redeem_invite(p_token uuid)
returns uuid  -- returns workspace_id on success
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invite    public.invites%rowtype;
  v_ws_type   text;
begin
  if auth.uid() is null then
    raise exception 'Not authenticated';
  end if;

  if not public.is_approved() then
    raise exception 'Account not approved';
  end if;

  -- Lock the invite row to prevent double-redemption under concurrent requests
  select * into v_invite
  from public.invites
  where token    = p_token
    and used_at  is null
    and expires_at > now()
  for update;

  if not found then
    raise exception 'Invalid or expired invite token';
  end if;

  select type into v_ws_type
  from public.workspaces
  where id = v_invite.workspace_id;

  if v_ws_type <> 'campaign' then
    raise exception 'Invite target is not a campaign workspace';
  end if;

  -- Add user as player; silently ignore if already a member
  insert into public.workspace_members (workspace_id, user_id, role)
  values (v_invite.workspace_id, auth.uid(), 'player')
  on conflict (workspace_id, user_id) do nothing;

  update public.invites
  set used_at = now()
  where id = v_invite.id;

  return v_invite.workspace_id;
end;
$$;


